function selectUser($id) {
    $conn = ConnexionPDO::$_connexionBdd;
    $sql = "SELECT * FROM UTILISATEUR "
            . "WHERE idUtilisateur = ?";
    $stmt = $conn->prepare($sql);
    $stmt->execute(array($id));
    
    
    if ($stmt->rowCount() > 0) {
        $row = $stmt->fetch();
        
        return $row;
    }
    
    return false;
       
}

