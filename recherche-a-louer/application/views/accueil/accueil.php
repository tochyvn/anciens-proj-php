    $(function(){
        
        // envoi du formulaire de connexion
        $('#form-connexion').submit(function(){
            var id = $(this).attr('id');
            var elem = $(this); 
            if( !form_validation( id ) ){
                return false;
            }
            $.ajax({
                type: "POST", 
                dataType: "json",
                data: $(this).serialize(), 
                url: $(this).attr('action'),
                error: function(){
                    elem.find('.msg_error').fadeIn('slow').delay(10000).fadeOut();
                },
                success: function(result) {
                    if( result == true ){
                        elem.find(".msg_success").slideToggle("slow");
                        elem.find('.msg_success').fadeIn('slow').delay(10000).fadeOut();
                    }
                    else{
                        elem.find('.msg_error').fadeIn('slow').delay(10000).fadeOut();
                    }
                }
            });
            return false;
        });
        
        
        function validateEmail( email ) {
            var re = /^([\w-]+(?:\.[\w-]+)*)@((?:[\w-]+\.)*\w[\w-]{0,66})\.([a-z]{2,6}(?:\.[a-z]{2})?)$/i;
            return re.test(email);
        }
        
        function form_validation( id ){
            var champs = $('#'+id+' .form_required');
            var validation = true;
            
            champs.each( function( index, value ){
                if( $(value).val() == '' || $(value).val() == null || typeof( $(value).val() ) == "undefined" ){
                    $(this).addClass('form_error');
                    validation = false;
                }
            });
            
            var email = $('#'+id+'.valid_email');
            email.each( function( index, value ){
                if( !validateEmail(  $(value).val() )){
                    $(this).addClass('form_error');
                    validation = false;
                }
            });  
            return validation;
        }
    });
